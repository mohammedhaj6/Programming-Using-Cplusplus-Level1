#include <iostream>
using namespace std;
int main()
{
	int a, b;
	cout << "Please enter the first number A?\n";
	cin >> a;
	cout << "Please enter the first number B?\n";
	cin >> b;
	cout << "\n\n";
	cout << a << " = " << b << " is " << (a==b)<<"\n";
	cout << a << " != " << b << " is " << (a != b) << "\n";
	cout << a << " > " << b << " is " << (a > b) << "\n";
	cout << a << " < " << b << " is " << (a < b) << "\n";
	cout << a << " >= " << b << " is " << (a >= b) << "\n";
	cout << a << " <= " << b << " is " << (a <= b) << "\n";




}

